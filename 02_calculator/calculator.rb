def add(number1, number2)
	result = number1 + number2
end


def subtract(number1, number2)
	result = number1 - number2
end

def sum(array)
	array.sum
	end



def multiply(number1, number2)
	result = number1 * number2
end 

def power(number1, number2)
	result = number1 ** number2
end

def factorial(number)
(1..number).inject(1) { |mem, var| mem * var }

end
